@dataclass
class _TileInfo:
    id_: int
    name: str
    palette: (int, int, int)


class _TileMeta(type):
    def __new__(mcs, name, bases, classdict):
        cls = super().__new__(mcs, name, bases, classdict)
        cls._tile_info_properties = {tile.id_: tile for tile in [
            value for key, value in classdict.items()
            if isinstance(value, _TileInfo)
        ]}
        return cls


